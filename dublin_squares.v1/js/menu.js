function renderHouseMenuItem(menuItems) {

    var html;

    html = '<td';
    if (menuItems.available) {
        html += ' class="active"';
    }
    html += '><a class="tableBtn" href="' + menuItems.destination + '">';
    html += menuItems.title + '</a></td>';

    return html;

}

